#include "ft_printf.h"

int	ft_get_nb_len(int nb)
{
	int	len_nb;

	len_nb = 0;
	if (nb <= 0)
		len_nb++;
	while (nb)
	{
		nb /= 10;
		len_nb++;
	}
	return (len_nb);
}
